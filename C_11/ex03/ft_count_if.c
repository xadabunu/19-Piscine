int	ft_count_if(char **tab, int length, int (*f)(char*))
{
	int	i;
	int	ctr;

	i = 0;
	ctr = 0;
	while (i < length)
	{
		if ((*f)(tab[i]) != 0)
			++ctr;
		++i;
	}
	return (ctr);
}
