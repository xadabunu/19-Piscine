int	ft_str_is_numeric(char *str)
{
	int	res;
	int	i;

	i = 0;
	res = 1;
	if (str[i] != '\0')
	{
		if (str[0] == '-' || str[i] == '+')
			++i;
		while (str[i] != '\0' && res == 1)
		{
			if (str[i] < '0' || str[i] > '9')
				res = 0;
			++i;
		}
	}
	return (res);
}
