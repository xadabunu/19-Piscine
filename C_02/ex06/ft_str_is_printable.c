int	ft_str_is_printable(char *str)
{
	int	i;
	int	res;

	res = 1;
	i = 0;
	if (str[i] != '\0')
	{
		while (str[i] != '\0')
		{
			if (str[i] < 32 || str[i] > 126)
				res = 0;
		++i;
		}
	}
	return (res);
}
